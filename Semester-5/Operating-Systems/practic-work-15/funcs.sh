#!/bin/bash

# Функція привітання
hello() {
    echo "Привіт! Ласкаво просимо до скрипта."
}

# Функція виводу поточної дати
today() {
    echo -n "Сьогоднішня дата: "
    date +"%d-%m-%Y %H:%M:%S"
}

# Функція виводу імені користувача
user() {
    echo "Поточний користувач: $USER"
}

# Виклик функцій по черзі
hello
today
user

