#!/bin/bash

choice=0

while [ "$choice" != "3" ]
do
    echo "--- ГОЛОВНЕ МЕНЮ ---"
    echo "1 — Показати дату"
    echo "2 — Показати список файлів"
    echo "3 — Вийти"
    echo "--------------------"
    echo -n "Виберіть опцію: "
    read choice

    case $choice in
        1)
            echo -n "Поточна дата та час: "
            date "+%d-%m-%Y %H:%M:%S"
            ;;
        2)
            echo "Список файлів у поточному каталозі:"
            ls -F
            ;;
        3)
            echo "Вихід зі скрипта. До побачення!"
            ;;
        *)
            echo "Помилка: невірний вибір. Спробуйте ще раз."
            ;;
    esac

    echo ""
done